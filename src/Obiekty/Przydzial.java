package Obiekty;

public class Przydzial {
	
	private int idPrzydzial;
	private int idPracownik;
	private int idPrzedmiot;
	
	
	public int getIdPrzydzial() {
		return idPrzydzial;
	}
	public void setIdPrzydzial(int idPrzydzial) {
		this.idPrzydzial = idPrzydzial;
	}
	public int getIdPracownik() {
		return idPracownik;
	}
	public void setIdPracownik(int idPracownik) {
		this.idPracownik = idPracownik;
	}
	public int getIdPrzedmiot() {
		return idPrzedmiot;
	}
	public void setIdPrzedmiot(int idPrzedmiot) {
		this.idPrzedmiot = idPrzedmiot;
	}
	
}
